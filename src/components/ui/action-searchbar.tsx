"use client"

import { useState, useEffect } from "react"
import { motion, AnimatePresence } from "framer-motion"
import { Search, Send } from "lucide-react"

function useDebounce<T>(value: T, delay = 500): T {
  const [debouncedValue, setDebouncedValue] = useState<T>(value)

  useEffect(() => {
    const timer = setTimeout(() => {
      setDebouncedValue(value)
    }, delay)

    return () => {
      clearTimeout(timer)
    }
  }, [value, delay])

  return debouncedValue
}

export interface SearchAction {
  id: string
  label: string
  icon?: React.ReactNode
  description?: string
  short?: string
  end?: string
  onClick?: () => void
}

interface SearchResult {
  actions: SearchAction[]
}

interface ActionSearchbarProps {
  actions?: SearchAction[]
  placeholder?: string
  label?: string
  onSearch?: (query: string) => void
  onActionSelect?: (action: SearchAction) => void
}

export function ActionSearchbar({
  actions = [],
  placeholder = "Поиск...",
  label = "Поиск",
  onSearch,
  onActionSelect
}: ActionSearchbarProps) {
  const [query, setQuery] = useState("")
  const [result, setResult] = useState<SearchResult | null>(null)
  const [isFocused, setIsFocused] = useState(false)
  const [selectedAction, setSelectedAction] = useState<SearchAction | null>(null)
  const debouncedQuery = useDebounce(query, 200)

  useEffect(() => {
    if (!isFocused) {
      setResult(null)
      return
    }

    if (!debouncedQuery) {
      setResult({ actions })
      onSearch?.("")
      return
    }

    const normalizedQuery = debouncedQuery.toLowerCase().trim()
    const filteredActions = actions.filter((action) => {
      const searchableText = action.label.toLowerCase()
      return searchableText.includes(normalizedQuery)
    })

    setResult({ actions: filteredActions })
    onSearch?.(debouncedQuery)
  }, [debouncedQuery, isFocused, actions, onSearch])

  const handleInputChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setQuery(e.target.value)
  }

  const container = {
    hidden: { opacity: 0, height: 0 },
    show: {
      opacity: 1,
      height: "auto",
      transition: {
        height: {
          duration: 0.4,
        },
        staggerChildren: 0.1,
      },
    },
    exit: {
      opacity: 0,
      height: 0,
      transition: {
        height: {
          duration: 0.3,
        },
        opacity: {
          duration: 0.2,
        },
      },
    },
  }

  const item = {
    hidden: { opacity: 0, y: 20 },
    show: {
      opacity: 1,
      y: 0,
      transition: {
        duration: 0.3,
      },
    },
    exit: {
      opacity: 0,
      y: -10,
      transition: {
        duration: 0.2,
      },
    },
  }

  const handleFocus = () => {
    setSelectedAction(null)
    setIsFocused(true)
  }

  const handleActionClick = (action: SearchAction) => {
    setSelectedAction(action)
    onActionSelect?.(action)
    action.onClick?.()
    setIsFocused(false)
  }

  return (
    <div className="w-full">
      <div className="relative flex flex-col justify-start items-center">
        <div className="w-full sticky top-0 bg-background z-10">
          {label && (
            <label className="text-xs font-medium text-muted-foreground mb-1 block" htmlFor="search">
              {label}
            </label>
          )}
          <div className="relative">
            <input
              id="search"
              type="text"
              placeholder={placeholder}
              value={query}
              onChange={handleInputChange}
              onFocus={handleFocus}
              onBlur={() => setTimeout(() => setIsFocused(false), 200)}
              className="w-full h-10 pl-3 pr-9 py-1.5 text-sm rounded-lg border border-border bg-background focus:outline-none focus:ring-2 focus:ring-ring transition-all placeholder:text-muted-foreground"
            />
            
            <div className="absolute right-3 top-1/2 -translate-y-1/2 h-4 w-4">
              <AnimatePresence mode="popLayout">
                {query.length > 0 ? (
                  <motion.div
                    key="send"
                    initial={{ y: -20, opacity: 0 }}
                    animate={{ y: 0, opacity: 1 }}
                    exit={{ y: 20, opacity: 0 }}
                    transition={{ duration: 0.2 }}
                  >
                    <Send className="w-4 h-4 text-muted-foreground" />
                  </motion.div>
                ) : (
                  <motion.div
                    key="search"
                    initial={{ y: -20, opacity: 0 }}
                    animate={{ y: 0, opacity: 1 }}
                    exit={{ y: 20, opacity: 0 }}
                    transition={{ duration: 0.2 }}
                  >
                    <Search className="w-4 h-4 text-muted-foreground" />
                  </motion.div>
                )}
              </AnimatePresence>
            </div>
          </div>
        </div>

        <div className="w-full">
          <AnimatePresence>
            {isFocused && result && !selectedAction && result.actions.length > 0 && (
              <motion.div
                className="w-full border rounded-md shadow-sm overflow-hidden border-border bg-background mt-1"
                variants={container}
                initial="hidden"
                animate="show"
                exit="exit"
              >
                <motion.ul>
                  {result.actions.map((action) => (
                    <motion.li
                      key={action.id}
                      className="px-3 py-2 flex items-center justify-between hover:bg-accent cursor-pointer rounded-md transition-colors"
                      variants={item}
                      layout
                      onClick={() => handleActionClick(action)}
                    >
                      <div className="flex items-center gap-2 justify-between">
                        <div className="flex items-center gap-2">
                          {action.icon && <span className="text-muted-foreground">{action.icon}</span>}
                          <span className="text-sm font-medium text-foreground">{action.label}</span>
                          {action.description && (
                            <span className="text-xs text-muted-foreground">{action.description}</span>
                          )}
                        </div>
                      </div>
                      <div className="flex items-center gap-2">
                        {action.short && <span className="text-xs text-muted-foreground">{action.short}</span>}
                        {action.end && <span className="text-xs text-muted-foreground text-right">{action.end}</span>}
                      </div>
                    </motion.li>
                  ))}
                </motion.ul>
              </motion.div>
            )}
          </AnimatePresence>
        </div>
      </div>
    </div>
  )
}
